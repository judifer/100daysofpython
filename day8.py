def dir(d, t, s):
    if d == "decode":
        s -= (2 * s)
    return ceasar(t, s)

def ceasar(t, s):
    shifted_text = ""
    for a in t:
        if a not in alphabet:
            shifted_text += a
        else:
            for idx, b in enumerate(alphabet):
                if a == b:
                    c = idx + s
                    if c > (len(alphabet) - 1):
                        c = c % (len(alphabet))
                    shifted_text += alphabet[c]
    return shifted_text

